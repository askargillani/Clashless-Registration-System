    public void loadDatesOnTable(String courseName)
    {   
        attendanceTable.removeAll();
        ArrayList<String> dates = s.getDates(courseName);
        ArrayList<String> status = s.getStatus(courseName);
        DefaultTableModel m= new DefaultTableModel();
        m.addColumn(new String("Date"));
        m.addColumn(new String("P/A"));
        attendanceTable.setModel(m);
        for(int i=0;i<dates.size();i++)
        {
            m.addRow(new Object[]{dates.get(i),status.get(i)});
        }
    }
    
    public void loadEvaluationsOnTable(String courseName)
    {   
        courseEvaluationTable.removeAll();
        ArrayList<String> evaluations = s.getEvaluations(courseName);
        ArrayList<String> marks = s.getMarks(courseName);
        DefaultTableModel m= new DefaultTableModel();
        m.addColumn(new String("Evaluation"));
        m.addColumn(new String("Marks"));
        courseEvaluationTable.setModel(m);
        for(int i=0;i<evaluations.size();i++)
        {
            m.addRow(new Object[]{evaluations.get(i),marks.get(i)});
        }
    }
    
    public void loadCoursesRegisteredOnAttendance()
    {
        coursesNames = s.getRegisteredCourses();
        for(int i=0;i<s.noOfRegistrations();i++)
        {
            coursesRegisteredComboBox.addItem(coursesNames[i]);
        }
        
    }
    
    public void loadCoursesRegisteredOnEvaluation()
    {
        coursesNames = s.getRegisteredCourses();
        for(int i=0;i<s.noOfRegistrations();i++)
        {
            courseEvaluatedComboBox.addItem(coursesNames[i]);
        }
        
    }
    
    public void loadCoursesOnTable()
    {
        String [] courseList = c.getAllCourses();
        DefaultListModel m = new DefaultListModel();
        for(int i=0;i<courseList.length;i++)
        {
            //System.out.println(courseList[i]);
            m.addElement(courseList[i]);
        }
        registrationCourses.setModel(m);
    }

