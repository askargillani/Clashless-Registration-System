    private void addBookButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addBookButtonMouseClicked
        // TODO add your handling code here:
        String name = bookNameTextField.getText();
        String note = bookDetailsTextArea.getText();
        System.out.println(note);
        if(!name.equals("") && !note.equals(""))
        {
            c.addBook(name, note);
            JOptionPane.showMessageDialog(this,"Thank you for helping other students!");
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Please enter the name and address correctly!");
        }
    }//GEN-LAST:event_addBookButtonMouseClicked

    private void lendBookButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lendBookButtonMouseClicked
        // TODO add your handling code here:
        String bookName = searchBookNameTextField.getText();
            String address = c.searchBook(bookName);
            if(address!=null)
            {
                bookSearched =true;
                c.lendBook(bookName);
                JOptionPane.showMessageDialog(this,"Address is "+address);
            }
            else
            {
                bookSearched=false;
                JOptionPane.showMessageDialog(this,"not available");
            }
    }//GEN-LAST:event_lendBookButtonMouseClicked

    private void searchBookButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchBookButtonMouseClicked
        // TODO add your handling code here:
        String bookName = searchBookNameTextField.getText();
        String address = c.searchBook(bookName);
        if(address!=null)
        {
            bookSearched =true;
            searchBookResults.setText(bookName + "\n" + address);
        }
        else
        {
            bookSearched=false;
            searchBookResults.setText("No results available");
        }
    }//GEN-LAST:event_searchBookButtonMouseClicked

